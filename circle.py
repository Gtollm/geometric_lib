import math


def area(r):
    '''Counts the area of a circle using the radius.

    Args:
        r (int): radius f the circle.

    Returns:
        Area of the square.
    '''
    return math.pi * r * r


def perimeter(r):
    '''Counts the perimeter of a circle using radius.

    Args:
        a (int): radius of circle.

    Returns:
        Perimeter of circle.
    '''
    return 2 * math.pi * r

