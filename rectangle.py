def area(a, b):
    '''Counts the area of a rectangle using length of two sides.

    Args:
        a (int): length of first side of a rectangle.
        b (int): length of second side of a rectangle.

    Returns:
        Area of the rectangle.
    '''
    return a * b

def perimeter(a, b):
    '''Counts the perimeter of a rectangle using length of two sides.

    Args:
        a (int): length of first side of a rectangle.
        b (int): length of second side of a rectangle.

    Returns:
        Perimeter of the rectangle.
    '''
    return 2 * (a + b)



