
def area(a):
    '''Counts the area of a square using the side length.

    Args:
      a (int): length of side of square.

    Returns:
      Area of the square.
    '''
    return a * a


def perimeter(a):
    '''Counts the perimeter of a square using the side length.

    Args:
        a (int): length of side of square.

    Returns:
        Perimeter of the square.
    '''
    return 4 * a
