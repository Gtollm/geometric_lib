def area(a, h):
    '''Counts area of a triangle using one side and height of a triangle.

    Args:
        a (int): length of side of a triangle.
        h (int): length of height of a triangle.

    Returns:
        Area of a triangle.
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''Counts perimeter of a triangle using it's three sides.

    Args:
        a (int): length of first side of a triangle.
        b (int): length of second side of a triangle.
        c (int): length of third side of a triangle.

    Returns:
        Perimeter of a triangle.
    '''
    return a + b + c



